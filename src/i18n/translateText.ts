const applyTemplateTranslations = (
  value: string,
  templates: Array<{pattern: RegExp; convert: (...args: string[]) => string}>,
): string | null => {
  for (const template of templates) {
    const match = value.match(template.pattern);
    if (match) {
      return template.convert(...match.slice(1));
    }
  }

  return null;
};

export const translateText = (value: string, language: Language): string => {
  if (!value) return value;

  const exact =
    language === 'en' ? EXACT_NL_TO_EN.get(value) : EXACT_EN_TO_NL.get(value);
  if (exact) return exact;

  if (language === 'en') {
    const templated = applyTemplateTranslations(value, NL_TEMPLATE_TO_EN);
    return templated ?? value;
  }

  const templated = applyTemplateTranslations(value, EN_TEMPLATE_TO_NL);
  return templated ?? value;
};

export const translateNode = (
  node: React.ReactNode,
  translate: (value: string) => string,
): React.ReactNode => {
  if (typeof node === 'string') {
    return translate(node);
  }

  if (Array.isArray(node)) {
    return node.map(child => translateNode(child, translate));
  }

  if (React.isValidElement(node) && node.props?.children) {
    return React.cloneElement(node, {
      ...node.props,
      children: translateNode(node.props.children, translate),
    });
  }

  return node;
};
